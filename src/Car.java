public class Car {
    // company that made the car
    private String make;

    // design of the car
    private String model;

    // when it was manufactured
    private int year;

    private int weight;

    public Car(String make, String model, int year, int weight) {
        this.make = make;
        this.model = model;
        this.year = year;
        this.weight = weight;
    }
}
